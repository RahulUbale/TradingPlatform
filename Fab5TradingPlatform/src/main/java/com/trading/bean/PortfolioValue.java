package com.trading.bean;

public class PortfolioValue {

	private double value;
	

	public PortfolioValue() {
		super();

	}

	public PortfolioValue(double value) {
		super();
		this.value = value;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
	
}
