package com.trading.bean;

public class PortfolioCost {

	private double cost;

	public PortfolioCost() {
		super();
	}

	public PortfolioCost(double cost) {
		super();
		this.cost = cost;
	}

	public double getCost() {
		return cost;
	}

	public void setCost(double cost) {
		this.cost = cost;
	}
	
}
